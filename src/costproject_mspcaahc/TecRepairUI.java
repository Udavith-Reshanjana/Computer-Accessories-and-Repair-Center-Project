    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String getRepairID = repairIDTxt.getText().trim();
        String getRepairDesc = repairDescTxt.getText().trim();
        String getRepairStatus = statusTxt.getSelectedItem().toString().trim();
        
        technician = new Technician();
        
        if (getRepairDesc.isEmpty()) {
            getRepairDesc = null;
        }
        if (getRepairID.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the repair ID");
        } 
        else if (technician.isRepairIDValid(getRepairID) == false) {
            JOptionPane.showMessageDialog(null, "Please enter a valid repair ID which follows REP/XXXXX pattern", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (technician.isRepairIDExist(getRepairID) == false) {
            JOptionPane.showMessageDialog(null, "This repair ID does not exist", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            if (getRepairStatus.equals("")) {
            technician.updateRepair(getRepairID, getRepairStatus);
            } else if (getRepairDesc != null) {
                technician.updateRepair(getRepairID, getRepairDesc, getRepairStatus);
            } else {
                technician.updateRepair(getRepairID, getRepairStatus);
            }
            repairIDTxt.setText("");
            repairDescTxt.setText("");
            customerIDTxt.setText("");
            statusTxt.setSelectedIndex(0);

            technician.showRepairData(repairDetailsTable);

            JOptionPane.showMessageDialog(null, "Repair information updated successfully");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

