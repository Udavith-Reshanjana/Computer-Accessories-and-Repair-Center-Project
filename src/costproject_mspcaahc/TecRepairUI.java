    private void repairDetailsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_repairDetailsTableMouseClicked
        // TODO add your handling code here:
        int row = repairDetailsTable.getSelectedRow();

        if (row >= 0) {
            String repairID = repairDetailsTable.getValueAt(row, 0).toString();
            String repairDescription = repairDetailsTable.getValueAt(row, 1).toString();
            String customerID = repairDetailsTable.getValueAt(row, 2).toString();
            String repairStatus = repairDetailsTable.getValueAt(row, 3).toString().trim();

            repairIDTxt.setText(repairID);
            repairDescTxt.setText(repairDescription);
            customerIDTxt.setText(customerID);
            statusTxt.setSelectedItem(repairStatus);
        }
    }//GEN-LAST:event_repairDetailsTableMouseClicked

